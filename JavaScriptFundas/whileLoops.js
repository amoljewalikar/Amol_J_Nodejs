console.log("---------while loop-------------");
let i = 1;                   // => initialization
while(i <= 5){              // => condition
    console.log("Hello");
    i++;                    // => increment or decrement
}

console.log("---------do-while loop-------------");
let j = 10;             // => initialization
do{
    console.log("Hiii...", j);
    j++;                // => increment or decrement
}while(j <= 5)          // => condition